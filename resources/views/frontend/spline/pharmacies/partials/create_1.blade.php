<script>
setTimeout(function() { 
                $("select").selectpicker();
                $(".bootstrap-select").click(function () {
                     $(this).addClass("open");
                }); 
            }, 500);
    $('#create_pharmacy_form').formValidation({
        framework: 'bootstrap',
        icon: {
            // valid: 'glyphicon glyphicon-ok',
            invalid: 'glyphicon glyphicon-remove',
            validating: 'glyphicon glyphicon-refresh'
        },
        fields: {
            nom_pharmacie: {
                validators: {
                    notEmpty: {
                        message: 'Le nom de la pharmacie est obligatoire.'
                    }
                }
            },
            secteur: {
                validators: {
                    notEmpty: {
                        message: 'Le secteur est obligatoire.'
                    }
                }
            }
        }
    }).on('success.form.fv', function(e) {
        e.preventDefault();

        $.ajax({
            url: '{{route("frontend.pharmacies.store")}}',
            type: 'POST',
            async: false,
            data: {
                nom_pharmacie : $('input[name="nom_pharmacie"]').val(),
                secteur : $('select[name="secteur"]').val()
            },
            success: function(result) {
                var table = $('#products-table');
                table.DataTable().draw();
                $('.modal').modal('toggle');
                aa = JSON.parse(result);
                $('select[name="pharmacie_id"]')
                    .append('<option value="' + aa.id + '">' + aa.nom + '</option>');
                $('.pharmacie_picker').selectpicker('refresh');
            }
        });
    });
</script>